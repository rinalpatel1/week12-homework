package practise;

public class ElegibleAge {

    //Main method to execute programme
    public static void main(String[] args) {
        // Voting eligible age
        /*
        BYTE:
        Range: -128 to 127
        Memory:1 byte=8 bits
        */
        Byte b = 18;
        System.out.println(b);
        Byte b1 = 127;
        System.out.println(b1);
        // voting non-eligible age
         /*
         SHORT:
         Range:-32768 to 32767
         Memory: 2 bytes=16 bits
          */
        Short s = 200;
        System.out.println(s);


    }






}
